  var mx = 0, my = 0, rx = 0, ry = 0;
  document.addEventListener('mousemove', function(e) {
    mx = e.clientX; my = e.clientY;
    cursor.style.left = mx + 'px';
    cursor.style.top = my + 'px';
  });
  function animateRing() {
    rx += (mx - rx) * 0.12;
    ry += (my - ry) * 0.12;
    ring.style.left = rx + 'px';
    ring.style.top = ry + 'px';
    requestAnimationFrame(animateRing);
  }
  animateRing();
  document.querySelectorAll('a, button').forEach(function(el) {
    el.addEventListener('mouseenter', function() { cursor.style.transform = 'translate(-50%,-50%) scale(2.5)'; ring.style.width = '48px'; ring.style.height = '48px'; });
    el.addEventListener('mouseleave', function() { cursor.style.transform = 'translate(-50%,-50%) scale(1)'; ring.style.width = '32px'; ring.style.height = '32px'; });
  });
  var reveals = document.querySelectorAll('.reveal');
  var observer = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry, i) {
      if (entry.isIntersecting) {
        setTimeout(function() { entry.target.classList.add('visible'); }, i * 80);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -60px 0px' });
  reveals.forEach(function(el) { observer.observe(el); });

  // CONTADOR ANIMADO
  function animateCounter(el) {
    var target = parseInt(el.getAttribute('data-target'));
    if (target === 0) return;
    var duration = 1800;
    var start = null;
    function step(timestamp) {
      if (!start) start = timestamp;
      var progress = Math.min((timestamp - start) / duration, 1);
      var ease = progress < 0.5 ? 2 * progress * progress : -1 + (4 - 2 * progress) * progress;
      el.textContent = Math.floor(ease * target);
      if (progress < 1) requestAnimationFrame(step);
      else el.textContent = target;
    }
    requestAnimationFrame(step);
  }

  var counterObserver = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
      if (entry.isIntersecting) {
        animateCounter(entry.target);
        counterObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.5 });

  document.querySelectorAll('.counter').forEach(function(el) {
    counterObserver.observe(el);
  });


  // Esconde pré-visualização de URL no canto do navegador sem afetar funcionalidade
  document.querySelectorAll('a').forEach(function(a) {
    var href = a.getAttribute('href');
    if (!href) return;
    // Ignora links do menu mobile e do nav para não conflitar com o menu
    if (a.closest('.mobile-menu') || a.closest('nav')) return;
    a.setAttribute('data-href', href);
    a.removeAttribute('href');
    a.style.cursor = 'pointer';
    a.addEventListener('click', function(e) {
      e.preventDefault();
      var target = a.getAttribute('target');
      var url = a.getAttribute('data-href');
      if (target === '_blank') { window.open(url, '_blank'); }
      else { window.location.href = url; }
    });
  });

  // ---- Menu mobile ----
  (function() {
    var toggle = document.getElementById('mobileToggle');
    var menu = document.getElementById('mobileMenu');
    if (!toggle || !menu) return;
    toggle.addEventListener('click', function(e) {
      e.stopPropagation();
      var isOpen = menu.classList.contains('open');
      menu.classList.toggle('open');
      toggle.classList.toggle('open');
      document.body.style.overflow = isOpen ? '' : 'hidden';
    });
    // Fechar ao clicar em link do menu
    menu.querySelectorAll('a').forEach(function(a) {
      a.addEventListener('click', function() {
        menu.classList.remove('open');
        toggle.classList.remove('open');
        document.body.style.overflow = '';
      });
    });
    // Fechar ao clicar fora
    document.addEventListener('click', function(e) {
      if (menu.classList.contains('open') && !menu.contains(e.target) && e.target !== toggle) {
        menu.classList.remove('open');
        toggle.classList.remove('open');
        document.body.style.overflow = '';
      }
    });
  })();

  // ---- Tema claro/escuro ----
  (function() {
    var root = document.documentElement;
    var btn = document.getElementById('themeToggle');
    // Verificar preferência salva
    var saved = localStorage.getItem('bagico-theme');
    // Verificar preferência do sistema
    var prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
    var theme = saved || (prefersDark ? 'dark' : 'light');
    root.setAttribute('data-theme', theme);
    if (btn) {
      btn.addEventListener('click', function() {
        var current = root.getAttribute('data-theme');
        var next = current === 'dark' ? 'light' : 'dark';
        root.setAttribute('data-theme', next);
        localStorage.setItem('bagico-theme', next);
      });
    }
  })();
</script>
